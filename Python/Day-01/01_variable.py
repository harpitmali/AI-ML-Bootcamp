# There are 4 variable (String, Integer, Float, Boolean)

# String
name = "Harpit"
food = "Apple"
email = "harpit@fake.com"

print(f"My name: {name}")
print(f"My favourite is {food}")
print(f"Email: {email}")

#Interger
age = 21
no_Of_Student = 50

print(f"I am {age} year old")
print(f"In My classroom there are {no_Of_Student} students")

#Float
distance = 5.5
price = 10.99

print(f"I ran {distance}km today")
print(f"Price of an Apple is {price}")

#Boolean
is_Student = True
is_Online = False

if is_Student:
    print("You are a Student")
else:
    print("You are NOT a Student")

if is_Online:
    print("Status: Online")
else:
    print("Offline")