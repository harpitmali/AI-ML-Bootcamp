name = "Harpit Mali"
age = 21
cgpa = 8.1
is_Student = True

# Type of each variable
print(type(name))
print(type(age))
print(type(cgpa))
print(type(is_Student))

# Type Casting -> we use float(), int(), bool(), str() to chnage the type of orignal variable
age = float(age)
cgpa = int(cgpa)
name = bool(name)
is_Student = str(is_Student)

print(type(age))
print(type(cgpa))
print(type(name))
print(type(is_Student))
